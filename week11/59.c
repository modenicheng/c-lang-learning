#include <stdio.h>

int main() {
  int n;
  scanf("%d", &n);

  // 如果n>3，则3*m至少为1200（四位数），无解
  if (n > 3) {
    printf("0,0,0\n");
    return 0;
  }

  int found = 0; // 标记是否找到解

  // 遍历十位a（1-9）和个位b（1-9）
  for (int a = 1; a <= 9; a++) {
    if (a == n)
      continue; // 十位不能等于百位n
    for (int b = 1; b <= 9; b++) {
      if (b == n || b == a)
        continue; // 个位不能等于百位n和十位a

      int m = 100 * n + 10 * a + b; // 第一个三位数
      int m2 = 2 * m;
      int m3 = 3 * m;

      // 检查m2和m3是否为三位数
      if (m2 < 100 || m2 > 999 || m3 < 100 || m3 > 999) {
        continue;
      }

      int count[10] = {0}; // 初始化计数数组（0-9）

      // 处理m的各位
      count[m / 100]++;       // 百位
      count[(m / 10) % 10]++; // 十位
      count[m % 10]++;        // 个位

      // 处理m2的各位
      count[m2 / 100]++;
      count[(m2 / 10) % 10]++;
      count[m2 % 10]++;

      // 处理m3的各位
      count[m3 / 100]++;
      count[(m3 / 10) % 10]++;
      count[m3 % 10]++;

      // 检查0是否出现
      if (count[0] != 0) {
        continue;
      }

      // 检查1-9是否每个数字恰好出现一次
      int valid = 1;
      for (int i = 1; i <= 9; i++) {
        if (count[i] != 1) {
          valid = 0;
          break;
        }
      }

      if (valid) {
        printf("%d,%d,%d\n", m, m2, m3);
        found = 1;
      }
    }
  }

  // 未找到任何解
  if (!found) {
    printf("0,0,0\n");
  }

  return 0;
}